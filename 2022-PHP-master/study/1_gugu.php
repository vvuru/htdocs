<?php
/*
1. 구구단 역방향으로 출력하기
ex)
9 x 9 = 81
9 x 8 = 72
9 x 7 = 63
...
----------
8 x 9 = 72
8 x 8 = 64
...
----------
...
2 x 1 = 2
(처음과 마지막에 점선 찍지 말 것
 */
for ($i = 9; $i >= 2; $i--) {

    for ($z = 9; $z >= 1; $z--) {
        print "$i * $z = " . $i * $z . "<br>";
    }
    if ($i > 2) {
        print "---------------<br>";
    };
}
